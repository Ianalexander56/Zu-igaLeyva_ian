#calculadora basica
num1 = 12
num2 = float(input("ingrese un numero (puede ser decimal): "))

#suma
suma = num1 + num2
print(f"la suma de {num1} + {num2} es: {suma}")

#resta
resta = num1 - num2
print(f"la resta de {num1} - {num2} es: {resta}")

#multiplicacion
multiplicacion = num1 * num2
print(f"la multiplicacion de {num1} * {num2} es {multiplicacion}")

#igualdad (==)
print(f"El numero {num1} es diferente de a {num2}? {num1 == num2}")

#desigualdad (!=)
print(f"¿El numero {num1} es diferente a {num2}? {num1 != num2} ")

#mayor que (>)
print(f"¿El numero {num1} es mayor que {num2}? {num1 > num2}")

#menor que (<)
print(f"¿El numero {num1} es menor que {num2}? {num1 < num2}")

#Mayor o igual que (>=)
print(f"El numero {num1} ess mayor o igual que {num2}? {num1 >= num2}")
