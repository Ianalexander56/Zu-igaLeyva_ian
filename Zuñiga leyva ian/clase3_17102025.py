#Ejercicio 1: Haz un programa que solicite al usuario ingresar 5 calificaciones y calcula el promedio

#Solicitar al usuario ingresar las 5 calificaciones
calificacion1 = float(input("ingresa calificacion 1: "))
calificacion2 = float(input("ingresa calificacion 2: "))
calificacion3 = float(input("ingresa calificacion 3: "))
calificacion4 = float(input("ingresa calificacion 4: "))
calificacion5 = float(input("ingresa calificacion 5: "))

#Calcular el promedio
promedio = (calificacion1 + calificacion2 + calificacion3 + calificacion4 + calificacion5) / 5

#Mostrar promedio 
print("El promedio de las calificaciones es: {promedio}")





